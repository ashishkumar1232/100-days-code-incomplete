def formatted_name(f_name,l_name):
    """this is the documentation for the function used in this case.and also it can be used as a multi liner comment"""
    formatted_f_name=f_name.title()
    formatted_l_name=l_name.title()
    print(f"{formatted_f_name} {formatted_l_name}")
formatted_name("ASHISH" ,"KUMAR")
